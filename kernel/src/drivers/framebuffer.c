// ---------------------------------------------------------------------------
// Terminal state
// ---------------------------------------------------------------------------
static struct {
  uint32_t *base;
  uint32_t width;
  uint32_t height;
  uint32_t pitch; // in pixels (bytes/4)

  uint32_t cols;
  uint32_t rows;

  uint32_t cursor_x;
  uint32_t cursor_y;

  uint32_t fg;
  uint32_t bg;

  // ANSI State Machine
  int ansi_state; // 0=NORMAL, 1=ESC, 2=CSI
  uint32_t ansi_params[8];
  int ansi_pcount;

  // UTF-8 State Machine
  int utf8_rem;     // remaining continuation bytes
  uint32_t utf8_cp; // accumulated codepoint
  int utf8_expect;  // total bytes expected
} term;

