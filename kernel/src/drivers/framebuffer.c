// ---------------------------------------------------------------------------
// Terminal state
// ---------------------------------------------------------------------------
static struct {
  uint32_t *base;
  uint32_t width;
  uint32_t height;
  uint32_t pitch;   // in pixels (bytes/4)

  uint32_t cols;
  uint32_t rows;

  uint32_t cursor_x;
  uint32_t cursor_y;

  uint32_t fg;
  uint32_t bg;

  // ANSI State Machine
  int ansi_state;          // 0=NORMAL, 1=ESC, 2=CSI
  uint32_t ansi_params[8];
  int ansi_pcount;

  // UTF-8 State Machine
  int utf8_rem;            // remaining continuation bytes
  uint32_t utf8_cp;        // accumulated codepoint
  int utf8_expect;         // total bytes expected
} term;

// ---------------------------------------------------------------------------
// UTF-8 → ASCII fallback map
// ---------------------------------------------------------------------------
static char utf8_map(uint32_t cp) {
  switch (cp) {
    // Dashes
    case 0x2014: case 0x2013: case 0x2010: case 0x2011: return '-';
    // Box drawing → simple ASCII
    case 0x2500: case 0x2501: case 0x2550: return '=';
    case 0x2502: case 0x2503: case 0x2551: return '|';
    case 0x250C: case 0x250F: case 0x2554: return '+';
    case 0x2510: case 0x2513: case 0x2557: return '+';
    case 0x2514: case 0x2517: case 0x255A: return '+';
    case 0x2518: case 0x251B: case 0x255D: return '+';
    case 0x251C: case 0x2523: case 0x2560: return '+';
    case 0x2524: case 0x252B: case 0x2563: return '+';
    case 0x252C: case 0x2533: case 0x2566: return '+';
    case 0x2534: case 0x253B: case 0x2569: return '+';
    case 0x253C: case 0x254B: case 0x256C: return '+';
    // Block elements → '#'
    case 0x2588: case 0x2589: case 0x258A: case 0x258B:
    case 0x258C: case 0x258D: case 0x258E: case 0x258F:
    case 0x2590: case 0x2591: case 0x2592: case 0x2593:
      return '#';
    // Arrows
    case 0x2190: return '<';
    case 0x2191: return '^';
    case 0x2192: return '>';
    case 0x2193: return 'v';
    // Bullets / dots
    case 0x2022: return '*';
    case 0xB7:   return '.';
    // Common Latin-1
    case 0xA9: return 'C'; // copyright
    default: return 0;     // unknown → will print '?'
  }
}

// ---------------------------------------------------------------------------
// Internal: draw a single glyph at (col, row)
// ---------------------------------------------------------------------------
static void draw_glyph(uint32_t col, uint32_t row, char ch) {
  uint8_t idx = (uint8_t)ch;
  if (idx > 0x7F) idx = '?';
  const uint8_t *glyph = font8x16[idx];
  uint32_t base_x = col * FONT_W;
  uint32_t base_y = row * FONT_H;

  for (uint32_t y = 0; y < FONT_H; y++) {
    uint8_t row_bits = glyph[y];
    for (uint32_t x = 0; x < FONT_W; x++) {
      uint32_t px = base_x + x;
      uint32_t py = base_y + y;
      if (px >= term.width || py >= term.height) continue;
      uint32_t color = (row_bits & (0x80 >> x)) ? term.fg : term.bg;
      term.base[py * term.pitch + px] = color;
    }
  }
}

// ---------------------------------------------------------------------------
// Internal: scroll the terminal up one line
// ---------------------------------------------------------------------------
static void scroll_up(void) {
  size_t row_pixels = (size_t)FONT_H * term.pitch;
  memmove(term.base, term.base + row_pixels,
          row_pixels * (term.rows - 1) * sizeof(uint32_t));

  uint32_t *last = term.base + (term.rows - 1) * FONT_H * term.pitch;
  for (uint32_t y = 0; y < FONT_H; y++)
    for (uint32_t x = 0; x < term.width; x++)
      last[y * term.pitch + x] = term.bg;
}

// ---------------------------------------------------------------------------
// ANSI color mapping
// ---------------------------------------------------------------------------
static uint32_t ansi_color_to_hex(uint32_t code) {
  switch (code) {
  case 30: return 0x000000;
  case 31: return 0xAA0000;
  case 32: return 0x00AA00;
  case 33: return 0xAA5500;
  case 34: return 0x0000AA;
  case 35: return 0xAA00AA;
  case 36: return 0x00AAAA;
  case 37: return 0xAAAAAA;
  case 90: return 0x555555;
  case 91: return 0xFF5555;
  case 92: return 0x55FF55;
  case 93: return 0xFFFF55;
  case 94: return 0x5555FF;
  case 95: return 0xFF55FF;
  case 96: return 0x55FFFF;
  case 97: return 0xFFFFFF;
  default: return term.fg;
  }
}

static void apply_ansi_sgr(void) {
  if (term.ansi_pcount == 0) {
    term.fg = 0xFFFFFF;
    term.bg = 0x000000;
    return;
  }
  for (int i = 0; i < term.ansi_pcount; i++) {
    uint32_t p = term.ansi_params[i];
    if (p == 0) {
      term.fg = 0xFFFFFF;
      term.bg = 0x000000;
    } else if ((p >= 30 && p <= 37) || (p >= 90 && p <= 97)) {
      term.fg = ansi_color_to_hex(p);
    } else if (p >= 40 && p <= 47) {
      term.bg = ansi_color_to_hex(p - 10);
    }
  }
}

// ---------------------------------------------------------------------------
// Public API
// ---------------------------------------------------------------------------

void fb_init(struct limine_framebuffer *fb) {
  term.base = (uint32_t *)fb->address;
  term.width = (uint32_t)fb->width;
  term.height = (uint32_t)fb->height;
  term.pitch = (uint32_t)(fb->pitch / 4);

  term.cols = term.width / FONT_W;
  term.rows = term.height / FONT_H;

  term.cursor_x = 0;
  term.cursor_y = 0;

  term.fg = FB_COLOR_WHITE;
  term.bg = FB_COLOR_BLACK;

  term.ansi_state = 0;
  term.ansi_pcount = 0;
  for (int i = 0; i < 8; i++) term.ansi_params[i] = 0;

  term.utf8_rem = 0;
  term.utf8_cp  = 0;
  term.utf8_expect = 0;

  fb_clear();
}

void fb_set_color(uint32_t fg, uint32_t bg) {
  term.fg = fg;
  term.bg = bg;
}

void fb_clear(void) {
  for (uint32_t y = 0; y < term.height; y++)
    for (uint32_t x = 0; x < term.width; x++)
      term.base[y * term.pitch + x] = term.bg;
  term.cursor_x = 0;
  term.cursor_y = 0;
}

void fb_put_pixel(uint32_t x, uint32_t y, uint32_t color) {
  if (x < term.width && y < term.height)
    term.base[y * term.pitch + x] = color;
}

void fb_get_size(uint32_t *cols, uint32_t *rows) {
  if (cols) *cols = term.cols;
  if (rows) *rows = term.rows;
}

// ---------------------------------------------------------------------------
// fb_putchar — now UTF-8 aware and ANSI hardened
// ---------------------------------------------------------------------------
void fb_putchar(char c) {
  if (!term.base) return;

  uint8_t uc = (uint8_t)c;

  // ---- UTF-8 handling -----------------------------------------------------
  if (uc >= 0x80) {
    if (term.utf8_rem == 0) {
      // New multi-byte sequence
      if ((uc & 0xE0) == 0xC0) { term.utf8_expect = 2; term.utf8_cp = uc & 0x1F; }
      else if ((uc & 0xF0) == 0xE0) { term.utf8_expect = 3; term.utf8_cp = uc & 0x0F; }
      else if ((uc & 0xF8) == 0xF0) { term.utf8_expect = 4; term.utf8_cp = uc & 0x07; }
      else { fb_putchar('?'); return; } // invalid start
      term.utf8_rem = term.utf8_expect - 1;
    } else {
      // Continuation byte expected
      if ((uc & 0xC0) == 0x80) {
        term.utf8_cp = (term.utf8_cp << 6) | (uc & 0x3F);
        term.utf8_rem--;
        if (term.utf8_rem == 0) {
          char rep = utf8_map(term.utf8_cp);
          fb_putchar(rep ? rep : '?');
        }
      } else {
        // Invalid sequence: abort and print '?'
        term.utf8_rem = 0;
        fb_putchar('?');
      }
    }
    return;
  }

  // ---- ANSI State Machine -------------------------------------------------
  if (term.ansi_state == 1) { // ESC seen
    if (c == '[') {
      term.ansi_state = 2;
      term.ansi_pcount = 0;
      term.ansi_params[0] = 0;
    } else {
      term.ansi_state = 0; // unknown escape, abort
    }
    return;
  } else if (term.ansi_state == 2) { // CSI seen
    if (c >= '0' && c <= '9') {
      term.ansi_params[term.ansi_pcount] =
          (term.ansi_params[term.ansi_pcount] * 10) + (c - '0');
    } else if (c == ';') {
      if (term.ansi_pcount < 7) {
        term.ansi_pcount++;
        term.ansi_params[term.ansi_pcount] = 0;
      }
    } else if (c == 'm') {
      term.ansi_pcount++;
      apply_ansi_sgr();
      term.ansi_state = 0;
    } else if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z')) {
      // Unsupported CSI command — silently drop
      term.ansi_state = 0;
    } else if (c == '\x1b') {
      // Another ESC inside CSI? abort current, start new
      term.ansi_state = 1;
    }
    // Any other char inside CSI (like cursor movement sequences)
    // We don't support them yet, but we must not get stuck.
    // If we see something weird, just stay in CSI until a letter arrives.
    return;
  }

  if (c == '\x1b') {
    term.ansi_state = 1;
    return;
  }

  // ---- Normal Drawing -----------------------------------------------------
  switch (c) {
  case '\n':
    term.cursor_x = 0;
    term.cursor_y++;
    break;
  case '\r':
    term.cursor_x = 0;
    break;
  case '\t':
    term.cursor_x = (term.cursor_x + 8) & ~7u;
    break;
  case '\b':
    if (term.cursor_x > 0) {
      term.cursor_x--;
      draw_glyph(term.cursor_x, term.cursor_y, ' ');
    }
    break;
  default:
    if (uc >= 0x20) {
      draw_glyph(term.cursor_x, term.cursor_y, c);
      term.cursor_x++;
    }
    break;
  }

  // Wrap columns
  if (term.cursor_x >= term.cols) {
    term.cursor_x = 0;
    term.cursor_y++;
  }

  // Scroll if needed
  while (term.cursor_y >= term.rows) {
    scroll_up();
    term.cursor_y--;
  }
}

void fb_puts(const char *s) {
  while (*s) fb_putchar(*s++);
}
